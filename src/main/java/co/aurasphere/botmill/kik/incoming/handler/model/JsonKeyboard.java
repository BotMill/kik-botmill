package co.aurasphere.botmill.kik.incoming.handler.model;

import java.util.List;

/**
 * The Class JsonKeyboard.
 */
public class JsonKeyboard {
	
	/** The id. */
	private String id;
	
	/** The type. */
	private String type;
	
	/** The responses. */
	private List<JsonResponse> responses;
	
	/**
	 * Gets the id.
	 *
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	
	/**
	 * Sets the id.
	 *
	 * @param id the new id
	 */
	public void setId(String id) {
		this.id = id;
	}
	
	/**
	 * Gets the type.
	 *
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	
	/**
	 * Sets the type.
	 *
	 * @param type the new type
	 */
	public void setType(String type) {
		this.type = type;
	}
	
	/**
	 * Gets the responses.
	 *
	 * @return the responses
	 */
	public List<JsonResponse> getResponses() {
		return responses;
	}
	
	/**
	 * Sets the responses.
	 *
	 * @param responses the new responses
	 */
	public void setResponses(List<JsonResponse> responses) {
		this.responses = responses;
	}
	
	
}
